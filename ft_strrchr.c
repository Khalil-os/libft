#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	slen;

	slen = 0;
	while (s[slen])
		slen++;
	i = slen - 1;
	if ((unsigned char)c == '\0')
		return ((char *)&s[slen]);
	while (i >= 0)
	{
		if ((unsigned char)s[i] == (unsigned char)c)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
